import pandas as pd

# ----------------------------
# 1. Загрузка данных
# ----------------------------
print("Загружаем данные из файла processed_with_b365_data.csv...")
df = pd.read_csv("processed_with_b365_data.csv")

# ----------------------------
# 2. Проверяем корректность загрузки
# ----------------------------
print("\nПервые 3 строки:")
print(df.head(3))

print("\nИнформация о датасете:")
print(df.info())

# ----------------------------
# 3. Обработка возможных ошибок
# ----------------------------

# Убираем лишние пробелы в строковых значениях
for col in df.select_dtypes(include='object').columns:
    df[col] = df[col].str.strip()

# Проверяем наличие пропусков
print("\nПропущенные значения в датасете:")
print(df.isnull().sum())